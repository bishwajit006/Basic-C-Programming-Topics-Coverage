// write  a programme that can count capacity of variable//
#include<stdio.h>
int main()
{
    char c;
    int i;
    float f;
    double d;
    printf("size of char %d\n",sizeof (c));
    printf("size of int %d\n",sizeof(i));
    printf("size of float %d\n",sizeof(f));
    printf("size of double %d",sizeof(d));
    return 0;

}
