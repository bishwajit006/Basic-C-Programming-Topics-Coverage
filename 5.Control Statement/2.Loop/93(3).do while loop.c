//so while
// 'do loop' has no any condition, but 'while loop' has condition

#include<stdio.h>
int main()
{
    int i=1; //initialization
    do  //has no condition
    {
        printf("My Name is Bishwajit Das\n");
        i++;  //increment
    }
    while(i<=10);  //condition
return 0;
}
