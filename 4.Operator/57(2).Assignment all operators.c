#include<stdio.h>
int main()
{

    int a=10,b=10,c=10,x=10,y=10;

    a+=2;//a+=2 mean a=a+2
    printf("sum = %d\n",a);

    b-=2;//b-=2 mean b=b-2
    printf("sub = %d\n",b);

    c*=5;//c*=5 mean c=c*5
    printf(" mul = %d\n",c);

    x/=5;  //x/=5 mean x=x/5
    printf(" div = %d\n",x);

    y%=3;  //y%=2 mean y=y%2
    printf(" mod = %d\n",y);// mod mean modulus is "%"  , eta vagsesh ber kore//

    return 0;


}

